async function main() {
  let count = 0;
  for (const line of lines) {
    if (!line.trim()) continue;
    
    // Some lines might just be separated by tabs or spaces
    // we split by tab
    const parts = line.split("\t");
    if (parts.length < 2) continue;
    
    const modelNameFull = parts[0].trim();
    const serialNumber = parts[1].trim();
    
    // Extract base model name and product id
    let productId = "";
    if (modelNameFull.includes("133 Pro Laptop")) productId = "133-pro-laptop";
    else if (modelNameFull.includes("155 Pro Laptop")) productId = "155-pro-laptop";
    else if (modelNameFull.includes("133 Pro AIO")) productId = "133-pro-aio";
    else if (modelNameFull.includes("155 Pro AIO")) productId = "155-pro-aio";
    else productId = modelNameFull.split(' ')[0].toLowerCase() + "-" + modelNameFull.split(' ')[1].toLowerCase();

    // Determine warranty months
    let warrantyMonths = 24; // Default to 24 unless specified 1Y
    if (modelNameFull.includes("1Y")) {
      warrantyMonths = 12;
    }
    
    // We insert or update
    await prisma.laptop.upsert({
      where: { serialNumber },
      update: {
        modelName: modelNameFull,
        productId,
        warrantyMonths,
      },
      create: {
        serialNumber,
        modelName: modelNameFull,
        productId,
        warrantyMonths,
        purchaseDate: new Date()
      }
    });
    count++;
  }
  
  console.log(`Successfully processed ${count} serial numbers.`);
}

main()
  .then(async () => {
    await prisma.$disconnect()
  })
  .catch(async (e) => {
    console.error(e)
    await prisma.$disconnect()
    process.exit(1)
  })
